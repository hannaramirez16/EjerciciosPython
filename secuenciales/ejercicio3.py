#variables
num1 = float(input("Introduce el primer número: ")) #En esta variable se define el valor del primer numero a sumar 
num2 = float(input("Introduce el segundo número: ")) #En esta variable se define el valor del segundo numero a sumar 
num3 = float(input("Introduce el tercer número: ")) #En esta variable se define el valor del tercer numero a sumar 
promedio= (num1 + num2 + num3) / 3 #en esta variable se realiza la operacion de la suma de los tres numero dividido por las tres notas
# Imprimir el mensaje utilizando las variables
print(f"El permedio de los numeros ingresados es: {promedio}") # Aqui se define el valor total de toda la operación
